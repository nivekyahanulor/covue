                    <h3 class="dark-blue-title">Declared Shipping Value for Foreign Online Sellers</h3>

                    <br>

                    <p><strong>Japan Import laws have changed!</strong></p>

                    <p>If you do not have a registered Japan company and you sell online in Japan, you are considered a Foreign Online Seller.</p>

                    <ol>
                        <li>All imports pay 10% Tax + Duty Tax</li>
                        <li>You cannot import (ship) any products into Japan without providing documentation (Online Account Report) that supports that your Declared Value is the Same as your Japan Online Selling Price.</li>
                    </ol>
                    <p>If you attempt to Import your product without the Online Selling Report, your shipment will be stopped in Customs. Your shipment will not be permitted to enter Japan.</p>

                    <h4 class="dark-yellow-title">What are the documents you need to provide?</h4>

                    <ol class="dark-yellow-title">
                        <li>FBA Commercial Invoice</li>
                        <li>Foreign Online Account Seller Report</li>
                        <li>FBA Simulator document</li>
                    </ol>

                    <br>

                    <p><strong>SHIPPING INVOICE</strong></p>

                    <p><strong>Step 1.</strong> Open your IOR Online Account and fill up the invoice generator</p>

                    <p><strong>Step 2.</strong> Fill in the necessary details.</p>

                    <p><strong>FOSR – Foreign Online Seller Report</strong></p>

                    <p><strong>Step 3.</strong> Check regular price and ASIN number on Amazon (If products are currently unavailable, please provide “manage inventory” page. Screenshot or print to pdf all products you are going to put on your FBA Commercial Shipping Invoice.</p>

                    <p><strong>Step 4:</strong> Save to pdf your Profile page. Seller Name must match the Company name. If not, provide the legal entity page.</p>

                    <p><strong>Step 5:</strong> Go back to your Invoice, and fill in the ASIN and Online Price</p>

                    <p><i>Image 1.1</i></p>

                    <p>Note: Online Selling Price must match your declared price in your commercial invoice.</p>

